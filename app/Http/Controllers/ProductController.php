    public  function showProduct($category, $article) {
        // get out productinRandomOrder()
        $product = Product::where('article', $article)->get();
        $price = $product[0] -> price;

        // get out similar(active) product
       $activeOffers = Product::where('categories_id', '=', $category)
           ->where('price', '<', $price)
           ->where('article', '!=', $product[0]->aticle)
           ->inRandomOrder()
           ->take(4)
           ->get();

        return view('home.product', ['product' => $product[0], 'activeOffers' => $activeOffers]);
    }
